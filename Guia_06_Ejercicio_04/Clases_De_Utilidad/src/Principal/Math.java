public class Math {
   
    /*
    CLASE MATH
    
    En ocasiones nos vemos en la necesidad de incluir cálculos, operaciones, matemáticas,
    estadísticas, etc en nuestros programas Java.
    Es cierto que muchos cálculos se pueden hacer simplemente utilizando los operadores
    aritméticos que java pone a nuestra disposición, pero existe una opción mucho más sencilla de
    utilizar, sobre todo para cálculos complicados. Esta opción es la clase Math del paquete java.lang.
    La clase Math nos ofrece numerosos y valiosos métodos y constantes estáticos, que podemos
    utilizar tan sólo anteponiendo el nombre de la clase.
    
        Método Descripción.
        
        abs(double a)               Devuelve el valor absoluto de un valor double introducido como
                                    parámetro.
        
        abs(int a)                  Devuelve el valor absoluto de un valor Entero introducido como
                                    parámetro.
        
        abs(long a)                 Devuelve el valor absoluto de un valor long introducido como
                                    parámetro.
        
        max(double a, double b)     Devuelve el mayor de dos valores double
        
        max(int a, int b)           Devuelve el mayor de dos valores Enteros.
        
        max(long a, long b)         Devuelve el mayor de dos valores long.
    
        min(double a, double b)     Devuelve el menor de dos valores double.
        
        min(int a, int b)           Devuelve el menor de dos valores enteros.
    
        min(long a, long b)         Devuelve el menor de dos valores long.
    
        pow(double a, double b)     Devuelve el valor del primer argumento elevado a la potencia del
                                    segundo argumento.
    
        random()                    Devuelve un double con un signo positivo, mayor o igual que 0.0 y
                                    menor que 1.0.
    
        round(double a)             Devuelve el long redondeado más cercano al double introducido.
                                
        sqrt(double a)              Devuelve la raíz cuadrada positiva correctamente redondeada de un
                                    double.
        
        floor(double a)             Devuelve el entero más cercano por debajo.
    */
}
